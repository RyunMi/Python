#编写一个numpy程序找出0-100里所有3，13，23的倍数，并输出(不能使用循环)
import numpy as np
A = np.arange(1,101)
B = A[2:100:3]
C = A[12:100:13]
D = A[22:100:23]
print("3的倍数有",B)
print("13的倍数有",C)
print("23的倍数有",D)
""" 结果：
3的倍数有 [ 3  6  9 12 15 18 21 24 27 30 33 36 39 42 45 48 51 54 57 60 63 66 69 72
 75 78 81 84 87 90 93 96 99]
13的倍数有 [13 26 39 52 65 78 91]
23的倍数有 [23 46 69 92] """
#A[2:100:3]为切片，表示从索引2搜索到索引100为止，间隔为3